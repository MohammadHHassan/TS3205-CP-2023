/* 	Exercise 1
	
	Create a program which prints out:
	
	Baa baa black sheep,
	Have you any wool?
	Yes sir, Yes sir,
	Three bags full.
*/

#include <stdio.h>			// Library for printf

int main(void)				// Main function
{
	printf("Baa baa black sheep,\n");		// Prints out Baa baa black sheep,
	printf("Have you any wool?\n");			// Prints out Have you any wool? on a new line
	printf("Yes sir, Yes sir,\n");			// Prints out Yes sir, Yes sir, on a new line
	printf("Three bags full.");				// Prints out Three bags full. on the last line
	
	return 0;
}
