// LearningWhile

#include <stdio.h>			// Library for printf and scanf

int main(void)				// Main function
{
	int k=1;
	
	while(k<=10)			// While condition k less than or equal to 100
	{
		printf("%d\n", k);	// Print out the value of k
		k++;				// Increment k by 1
	}
	
	printf("Thank you :)");
	
	return 0;				// End of main function
}
